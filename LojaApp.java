    private void jComprarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComprarButtonActionPerformed
        // TODO add your handling code here:
        if (jNomeBox.getSelectedItem() != null) {
            String nomeProdutoSelecionado = (String) jNomeBox.getSelectedItem();
            Produto produtoSelecionado = estoque.stream().filter(p -> p.getNome().equals(nomeProdutoSelecionado)).findFirst().orElse(null);
            // Neste if criei um sistema para descontar do estoque e atualiza
            if (produtoSelecionado != null && produtoSelecionado.getEstoque() > 0) {
                produtoSelecionado.setEstoque(produtoSelecionado.getEstoque() - 1);
                
                // O programa tambem oferece a opção de voltar para a tela inicial do usuario que realizou a compra
                int resposta = JOptionPane.showConfirmDialog(null, "Compra efetuada com sucesso! Deseja retornr para a tela inicial?",
                        "Compra confirmada.", JOptionPane.YES_NO_OPTION);
                if (resposta == JOptionPane.YES_OPTION) {
                    salvar_tela((Cliente) it.next());
                }
                // Caso o produto não possua mais estoque ou não foi escolhido, essas mensagens vão aparecer
            } else {
                JOptionPane.showMessageDialog(null, "Produto fora de estoque.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum produto selecionado.");
    }
    
    }//GEN-LAST:event_jComprarButtonActionPerformed
