    // Sistema de Limpar a Tela
    
    private void limpar_tela(){

    jNomeText.setText("");
    jCPFText.setText("");
    jEmailText.setText("");
    jEnderecoText.setText("");
    jTipoBox.setSelectedIndex(-1);
    jNomeBox.setSelectedIndex(-1);
    metodoComboBoxes();
    
    it=cliente.iterator();
    indice=0;
    
    }
    
    // Sistema de Salvar Tela
    
    private void salvar_tela(Cliente clienteSalvar){
        
    jNomeText.setText(clienteSalvar.getNome());
    jCPFText.setText(clienteSalvar.getCpf());
    jEmailText.setText(clienteSalvar.getEmail());
    jEnderecoText.setText(clienteSalvar.getEndereco());
    jTipoBox.setSelectedIndex(-1);
    jNomeBox.setSelectedIndex(-1);
    metodoComboBoxes();
    
    
    }
    
