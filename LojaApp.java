    // Nesta parte criei uma forma de atualizar e filtrar os produtos do estoque
    // para mostrar tambem na tela

    private void atualizaComboBox(String tipoSelecionado) {
        List<String> produtosFiltragem = estoque.stream()
            .filter(produto -> produto.getTipo().equals(tipoSelecionado))
            .map(Produto::getNome)
            .collect(java.util.stream.Collectors.toList());
        jNomeBox.setModel(new javax.swing.DefaultComboBoxModel<>(produtosFiltragem.toArray(new String[0])));
    }

